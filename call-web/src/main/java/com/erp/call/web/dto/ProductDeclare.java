package com.erp.call.web.dto;

public class ProductDeclare {

    private String currency;

    private String declareCode;

    private String name;

    private String nameEn;

    private Double price;

    private String tagBatteries;

    private String tagLiquidCosmetic;

    private String tagMagnetic;

    private String tagPowdered;

    private String tagProhibited;

    private String tagTort;

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public String getDeclareCode() {
        return declareCode;
    }

    public void setDeclareCode(String declareCode) {
        this.declareCode = declareCode;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNameEn() {
        return nameEn;
    }

    public void setNameEn(String nameEn) {
        this.nameEn = nameEn;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public String getTagBatteries() {
        return tagBatteries;
    }

    public void setTagBatteries(String tagBatteries) {
        this.tagBatteries = tagBatteries;
    }

    public String getTagLiquidCosmetic() {
        return tagLiquidCosmetic;
    }

    public void setTagLiquidCosmetic(String tagLiquidCosmetic) {
        this.tagLiquidCosmetic = tagLiquidCosmetic;
    }

    public String getTagMagnetic() {
        return tagMagnetic;
    }

    public void setTagMagnetic(String tagMagnetic) {
        this.tagMagnetic = tagMagnetic;
    }

    public String getTagPowdered() {
        return tagPowdered;
    }

    public void setTagPowdered(String tagPowdered) {
        this.tagPowdered = tagPowdered;
    }

    public String getTagProhibited() {
        return tagProhibited;
    }

    public void setTagProhibited(String tagProhibited) {
        this.tagProhibited = tagProhibited;
    }

    public String getTagTort() {
        return tagTort;
    }

    public void setTagTort(String tagTort) {
        this.tagTort = tagTort;
    }
}
